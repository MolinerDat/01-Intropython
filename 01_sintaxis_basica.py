# Introducción a Python: Sintaxis básica y tipos de datos

# Comentario: Esto no se ejecuta, sirve para explicar

# Imprimir texto
print("Hola, mundo")

# Crear variables
nombre = "Ana"
edad = 30
es_analista = True

# Mostrar variables
print("Nombre:", nombre)
print("Edad:", edad)
print("¿Es analista?", es_analista)

# Tipos de datos
print(type(nombre))   # str
print(type(edad))     # int
print(type(es_analista))  # bool

# Operaciones básicas
suma = 5 + 3
division = 10 / 2
potencia = 2 ** 3

print("Suma:", suma)
print("División:", division)
print("Potencia:", potencia)

# Concatenación de texto
mensaje = "Hola " + nombre + ", tienes " + str(edad) + " años."
print(mensaje)
